WIDTH = 1200
HEIGHT = 675
# number of rows and columns in the tile map
TILE_ROWS = 11.25
TILE_COLS = 20
TILE_DIM = WIDTH / TILE_COLS
GRAVITY = 0.8

class ImageInfo:
    """
    Image class which stores image attributes
    """
    def __init__(self, center, size, radius=0, lifespan=None, animated=False):
        self.center = center
        self.size = size
        self.radius = radius
        if lifespan:
            self.lifespan = lifespan
        else:
            self.lifespan = float('inf')
        self.animated = animated

    def get_center(self):
        return self.center

    def get_size(self):
        return self.size

    def get_radius(self):
        return self.radius

    def get_lifespan(self):
        return self.lifespan

    def get_animated(self):
        return self.animated
